package com.example.nemo1.view_customview.entity;

public class eDemo6 {
    private int album;
    private String tenhac;
    private String loinhac;

    public eDemo6(int album, String tenhac, String loinhac) {
        this.album = album;
        this.tenhac = tenhac;
        this.loinhac = loinhac;
    }

    public int getAlbum() {
        return album;
    }

    public void setAlbum(int album) {
        this.album = album;
    }

    public String getTenhac() {
        return tenhac;
    }

    public void setTenhac(String tenhac) {
        this.tenhac = tenhac;
    }

    public String getLoinhac() {
        return loinhac;
    }

    public void setLoinhac(String loinhac) {
        this.loinhac = loinhac;
    }
}
